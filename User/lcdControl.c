void BIT8_Set_All(uchar data1)
{
    int i;
    uchar temp = 0x80;
    
    for(i=7;i>=0;i--)
    //for(i=0;i<=7;i++)
    {
      if(data1&temp)
      {
          BIT8_Set_Bit(i,1);
      }
      else
      {
          BIT8_Set_Bit(i,0);
      }
      data1<<=1;
    }
}


void WriteCom(uchar data1)
{
    
   RS_Low;
   CS_Low;
   RD_High;
   WR_Low;
   BIT8_Set_All(data1);
   WR_High;
   CS_High;
}

void WriteData(uchar data1)
{
   RS_High;
   CS_Low;
   RD_High;
   WR_Low;
   BIT8_Set_All(data1);
   WR_High;
   CS_High;
} 


//定义窗口坐标：开始坐标（XS,YS)以及窗口大小（x_total,y_total)
void lcd_address(int XS,int YS,int x_total,int y_total)
{
	int XE,YE; 
	XE=XS+x_total-1;
	YE=YS+y_total-1;	
	WriteCom(0x2a);		// 设置X开始及结束的地址
	data_out_16(XS+2);	// X开始地址(16位）
	data_out_16(XE+2);	// X结束地址(16位）
			
	WriteCom(0x2b);		// 设置Y开始及结束的地址
	data_out_16(YS+3);	// Y开始地址(16位）
	data_out_16(YE+3);	// Y结束地址(16位）

	WriteCom(0x2c);	    // 写数据开始
}

//传送8位的数据
void data_out(uchar data1)
{
	//8080 8bit interface
	CS_Low;
	RS_High;
	RD_High;
	BIT8_Set_All(data1);
	WR_Low;
	WR_High;
	CS_High;        
}


//传16位数据，16位数据一起赋值
void data_out_16(int data_16bit)
{
	data_out(data_16bit >>8);
	data_out(data_16bit );
}

